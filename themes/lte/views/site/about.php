                    <p class="text-justify">Berbekal pengalaman mengelola lembaga PRISMA dengan metode SEMPOA maka pada awal tahun 2003 Zaenal mulai mengembangkan metode berhitung dengan Jari Tangan sehingga tidak membutuhkan alat bantu lagi. Kemudian tidak ada rumus-rumus tetapi langsung dengan metode penerapan berhitung. Selanjutnya cara penyampaian materi dengan bermain sambil belajar sehingga Anak akan sangat senang untuk menyukai berhitung dan matematika sejak dini. Metode tersebut diberi nama "KALKULATOR TANGAN", metode ini memang lahir karena terinspirasi oleh metode Sempoa manik 4 dan 1 sehingga dijadikan simbol dalam tangan seolah-olah simbol manik 4 dan 1. Metode Kalkulator Tangan mulai di kenalkan dimulai dari 2 sekolah TK yang ada di Wonosobo, ternyata hasilnya sangat menakjubkan untuk ukuran Anak TK anak sudah bisa berhitung angka-angka dibawah 100 sampai lebih dari 5 deret , contoh 17+18+39-25-16=.........., angka- angka tersebut sangat sulit dikerjakan oleh Anak usia TK tetapi dengan metode Kalkulator Tangan soal tersebut bisa sangat cepat diselesaikan dengan hanya menggunakan jari tangan. Kemudian anak juga merasa sangat senang dan tidak cepat bosan.</p>
